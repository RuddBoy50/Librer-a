     private void cargarDatos() {
        try {
            cn = conexion.abrirConexion();
            CallableStatement cs = cn.prepareCall("{call MostrarLibrosActivos()}");
            ResultSet rs = cs.executeQuery();

            // Crear el modelo de la tabla
            DefaultTableModel model = new DefaultTableModel();
            model.addColumn("ID");
            model.addColumn("Libro");
            model.addColumn("GÃ©nero");
            model.addColumn("Editorial");
            model.addColumn("Idioma");
            model.addColumn("Precio");
            model.addColumn("Existencias");

            // Agregar filas al modelo
            while (rs.next()) {
                Object[] fila = new Object[7];
                fila[0] = rs.getInt("Libro_Id");
                fila[1] = rs.getString("Libro_Libro");
                fila[2] = rs.getString("Genero_Genero");
                fila[3] = rs.getString("Editorial_Editorial");
                fila[4] = rs.getString("Idioma_Idioma");
                fila[5] = rs.getDouble("Libro_Precio");
                fila[6] = rs.getInt("Libro_Existencias");
                model.addRow(fila);
            }

            // Asignar el modelo a la tabla
            tbl_consulta.setModel(model);

            // Cerrar recursos
            rs.close();
            cs.close();
            conexion.cerrarConexion();

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Error al cargar los libros: " + e.getMessage());
        }
    }
    
    
    
    

